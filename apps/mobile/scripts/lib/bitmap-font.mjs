export function measureText(text, scale, letterSpacing = 1) {
  const upper = text.toUpperCase();
  const gap = Math.max(1, Math.round(scale * letterSpacing));
  let w = 0;
  for (let i = 0; i < upper.length; i += 1) {
    w += GLYPH_W * scale;
    if (i < upper.length - 1) w += gap;
  }
  return w;
}

// Paints `text` into a flat RGBA buffer of dimensions width×height.
export function drawText(pixels, width, height, text, x, y, scale, color, letterSpacing = 1) {
  const upper = text.toUpperCase();
  const gap = Math.max(1, Math.round(scale * letterSpacing));
  const [r, g, b, a = 255] = color;
  let cursorX = x;
  for (let ci = 0; ci < upper.length; ci += 1) {
    const glyph = FONT[upper[ci]] ?? FONT[" "];
    for (let row = 0; row < GLYPH_H; row += 1) {
      for (let col = 0; col < GLYPH_W; col += 1) {
        if (glyph[row][col] !== "1") continue;
        for (let sy = 0; sy < scale; sy += 1) {
          for (let sx = 0; sx < scale; sx += 1) {
            const px = cursorX + col * scale + sx;
            const py = y + row * scale + sy;
            if (px < 0 || px >= width || py < 0 || py >= height) continue;
            const off = (py * width + px) * 4;
            pixels[off] = r;
            pixels[off + 1] = g;
            pixels[off + 2] = b;
            pixels[off + 3] = a;
          }
        }
      }
    }
    cursorX += GLYPH_W * scale + gap;
  }
  return cursorX;
}
